# What is exception handling and what problem does it solve?


# ANSWER:
#
# Exception handling is a way to return a result when something
# goes wrong. This way the program will not stop in case of error.


# SOLUTION:
# 
# Exception handling is a structure used to handle the possibility
# of an error occurring in a program. It is a way of handling the
# error by changing the flow of control without exiting the program entirely.